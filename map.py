# Función auxiliar para rellenar los chunks vacíos (para que el código corra)
# Cuando vayas a editar un chunk, BORRA la llamada a esta función y pega la matriz.
def crear_chunk_vacio(es_top):
    chunk = []
    for i in range(18):
        if i < 7 and es_top: row = [0]*240 # Relleno Arriba
        elif i == 7 and es_top: row = [0]*240 # Borde Techo
        elif i == 10 and not es_top: row = [0]*240 # Borde Suelo
        elif i > 10 and not es_top: row = [0]*240 # Relleno Abajo
        else: row = [0]*240 # Aire
        chunk.append(row)
    return chunk

