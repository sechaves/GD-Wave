import pygame

class TypeObject:
    def __init__(self, x, y, arr, block_size):
        self.start_x = x 
        self.y = y
        self.block_size = block_size
        self.arr = arr 
        self.tiles = [] 
        self.col_index = 0          
        self.generated_x = x        
        self.screen_width_buffer = 2100 
        self.map_width = len(arr[0]) 
        self.update(0) 

    def update(self, speed):
        for tile in self.tiles:
            tile['rect'].x -= speed
        self.tiles = [t for t in self.tiles if t['rect'].right > -50]
        
        self.generated_x -= speed
        while self.generated_x < self.screen_width_buffer and self.col_index < self.map_width:
            for row_index, row in enumerate(self.arr):
                if row_index >= len(self.arr): break
                val = row[self.col_index] 
                if val != 0:
                    pos_y = self.y + (row_index * self.block_size)
                    rect = pygame.Rect(self.generated_x, pos_y, self.block_size, self.block_size)
                    self.tiles.append({'rect': rect, 'type': val})
            self.generated_x += self.block_size
            self.col_index += 1

